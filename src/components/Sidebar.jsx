export default function Sidebar({ collapsed }) {
  const location = useLocation();
  const [openGroups, setOpenGroups] = useState({ assets: true });

  const isGroupActive = (item) =>
    item.children?.some((c) => location.pathname === c.to);

  const toggleGroup = (key) =>
    setOpenGroups((prev) => ({ ...prev, [key]: !prev[key] }));

  return (
    <aside className={`sidebar${collapsed ? ' sidebar--collapsed' : ''}`}>
      <nav className="sidebar-nav">
        {NAV_ITEMS.map((item) => {
          if (item.children) {
            const open = openGroups[item.key];
            const active = isGroupActive(item);
            return (
              <div key={item.key}>
                <button
                  className={`sidebar-item sidebar-item--group${active ? ' sidebar-item--active' : ''}`}
                  onClick={() => toggleGroup(item.key)}
                >
                  <span className="sidebar-icon">{item.icon}</span>
                  {!collapsed && (
                    <>
                      <span className="sidebar-label">{item.label}</span>
                      <span className={`sidebar-chevron${open ? ' sidebar-chevron--open' : ''}`}>
                        <svg width="12" height="12" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2.5" strokeLinecap="round" strokeLinejoin="round">
                          <polyline points="6 9 12 15 18 9"/>
                        </svg>
                      </span>
                    </>
                  )}
                </button>
                {open && !collapsed && (
                  <div className="sidebar-children">
                    {item.children.map((child) => (
                      <NavLink
                        key={child.key}
                        to={child.to}
                        className={({ isActive }) =>
                          `sidebar-child${isActive ? ' sidebar-child--active' : ''}`
                        }
                      >
                        <span className="sidebar-icon sidebar-icon--sm">{child.icon}</span>
                        <span className="sidebar-label">{child.label}</span>
                      </NavLink>
                    ))}
                  </div>
                )}
              </div>
            );
          }

          if (item.to) {
            return (
              <NavLink
                key={item.key}
                to={item.to}
                end={item.exact}
                className={({ isActive }) =>
                  `sidebar-item${isActive ? ' sidebar-item--active' : ''}`
                }
                title={collapsed ? item.label : undefined}
              >
                <span className="sidebar-icon">{item.icon}</span>
                {!collapsed && <span className="sidebar-label">{item.label}</span>}
              </NavLink>
            );
          }

          // No route — placeholder item
          return (
            <div key={item.key} className="sidebar-item sidebar-item--disabled" title={collapsed ? item.label : undefined}>
              <span className="sidebar-icon">{item.icon}</span>
              {!collapsed && <span className="sidebar-label">{item.label}</span>}
            </div>
          );
        })}
      </nav>
    </aside>
  );
}
